// Given word = "ABCCED", return true.
// Given word = "SEE", return true.
// Given word = "ABCB", return false.

var exist = function(board, word, ans = false) {
    loop1: for (let i = 0; i < board.length; i++) {
        for (let j = 0; j < board[i].length; j++) {
            if (!ans && board[i][j] === word[0]) {
                ans = helper(board, word, i, j);
            }
            if (ans) {
                break loop1;
                break;
            }
        }
    }
    console.log(ans);
    return ans;
};

function helper(board, word, row, column, ind = 0, ans = [false]) {
    if (board[row] && board[row][column] === word[ind]) {
        if (ind === word.length - 1) {
            ans[0] = true;
        } else if (!ans[0]) {
            let current = board[row][column];
            board[row][column] = null;
            if (board[row][column - 1] === word[ind + 1]) {
                helper(board, word, row, column - 1, ind + 1, ans);
            }
            if (board[row - 1] && board[row - 1][column] === word[ind + 1]) {
                helper(board, word, row - 1, column, ind + 1, ans);
            }
            if (board[row + 1] && board[row + 1][column] === word[ind + 1]) {
                helper(board, word, row + 1, column, ind + 1, ans);
            }
            if (board[row][column + 1] === word[ind + 1]) {
                helper(board, word, row, column + 1, ind + 1, ans);
            }
            board[row][column] = current;
        }
    }
    return ans[0];
}

